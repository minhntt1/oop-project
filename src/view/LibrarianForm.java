    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
		// TODO add your handling code here:
		Object[][] status = new Object[][]{
			//FormValidation.validateId(jTextField24),
			FormValidation.validateName(jTextField19)
		};

		if (Arrays.stream(status).allMatch(x -> (Integer) x[0] == -1)) {
			Object[] statusAdd = PublisherAuthorManagement.getObject().addPublisher(String.valueOf(status[0][1]));

			if ((Integer) statusAdd[0] == -1) {
				Publisher newp = ((Publisher) statusAdd[1]);
				this.tblPublisher.addRow(newp.toObject());
				this.boxPublisher.addElement(newp.toString());
				JOptionPane.showMessageDialog(this, "Thêm NXB thành công");
			} else {
				JOptionPane.showMessageDialog(this, StatusCode.pamCode[(Integer) statusAdd[0]]);
			}
		} else {
			Object[] o = Arrays.stream(status).filter(x -> (Integer) x[0] != -1).findFirst().orElse(new Object[]{-2});
			JOptionPane.showMessageDialog(this, (Integer) o[0] == -2 ? "Unknown Error" : StatusCode.formCode[(Integer) o[0]]);
		}
    }//GEN-LAST:event_jButton13ActionPerformed

