    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
		// TODO add your handling code here:

		Object[][] val = new Object[][]{
			FormValidation.validateId(jTextField18),
			FormValidation.validateRelNo(jTextField13),
			FormValidation.validateCopies(jTextField17)
		};

		int id = (Integer) val[0][0] == -1 ? (Integer) val[0][1] : -1;
		int relNo = (Integer) val[1][0] == -1 ? (Integer) val[1][1] : -1;
		int pId = Integer.parseInt(String.valueOf(this.jComboBox8.getSelectedItem()).split("-")[0]);
		pId = pId == 0 ? -1 : pId;
		int copies = (Integer) val[2][0] == -1 ? (Integer) val[2][1] : -1;

		if (jComboBox7.getSelectedIndex() == 0) {
			String bName = this.jTextField14.getText();
			int[] idx = jList2.getSelectedIndices();
			int[] aid = idx.length == 0
					? null
					: Arrays.stream(idx)
							.boxed()
							.map(x -> Integer.valueOf(String.valueOf(listAuthor.get(x)).split("-")[0]))
							.mapToInt(Integer::intValue)
							.toArray();

			int pageNo = jTextField15.getText().matches("^\\d+$") ? Integer.parseInt(jTextField15.getText()) : -1;

			ArrayList<Document> res = DocumentManagement.getObject().findBook(id, relNo, pId, copies, bName, aid, pageNo);
			tblBook.setRowCount(0);

			for (Document doc : res) {
				Object[] bo = ((Book) doc).toObject();
				String s = "<html>";
				Object[] au = ((Object[]) bo[4]);

				for (int i = 0; i < au.length; ++i) {
					s = s.concat(((Author) au[i]).toString());
					if (i != au.length - 1) {
						s = s.concat("<br>");
					}
				}

				s = s.concat("</html>");
				bo[4] = s;

				this.tblBook.addRow(bo);
				this.jTable5.setRowHeight(jTable5.getRowCount() - 1, 20 * au.length);
			}
		} else {
			Object[] status = FormValidation.validateDateNoTime(jTextField16);
			Date relDate = (Integer) status[0] == -1 ? (Date) status[1] : null;

			ArrayList<Document> res = DocumentManagement.getObject().findPaper(id, relNo, pId, copies, relDate);

			tblPaper.setRowCount(0);
			for (Document d : res) {
				tblPaper.addRow(((Paper) d).toObject());
			}
		}
    }//GEN-LAST:event_jButton12ActionPerformed

